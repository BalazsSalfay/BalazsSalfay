public class TwoNumbers {
    public static void main(String[] args) {

        int firstNumber = 22;
        int secondNumber = 13;


        int added = firstNumber + secondNumber;
        System.out.println(added);


        int substracted = firstNumber - secondNumber;
        System.out.println(substracted);


        int multiple = firstNumber * secondNumber;
        System.out.println(multiple);



        float firstNumber2 = 22;
        float secondNumber2 = 13;

        float divided = firstNumber2 / secondNumber2;
        System.out.println(divided);



        int divided2 = firstNumber / secondNumber;
        System.out.println(divided2);




        int reminder = firstNumber % secondNumber;
        System.out.println(reminder);


    }
}
